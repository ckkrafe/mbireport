package com.mbireport.mbireport.DTO;

public class InspectUnitDTO
{
    private String name;
    private String address;
    private String mbi;
    private String umb;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMbi() {
        return mbi;
    }

    public void setMbi(String mbi) {
        this.mbi = mbi;
    }

    public String getUmb() {
        return umb;
    }

    public void setUmb(String umb) {
        this.umb = umb;
    }
}
